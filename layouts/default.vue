  data: function() {
    return {
      dark: false,
      invert: false
    };
  },
  methods: {
    switchDark() {
      this.dark = !this.dark;
    },
    switchInvert() {
      this.invert = !this.invert;
    }
  },
  computed: {
    setTheme() {
      if (this.dark) {
        return (this.$vuetify.theme.dark = true);
      } else {
        return (this.$vuetify.theme.dark = false);
      }
    }
  }
};
